package com.example.myapplication_amit.Adapters;

public class PojoGrid {

    private String title;
    private int images;

    public PojoGrid(String title, int images) {
        this.title = title;
        this.images = images;
    }

    public String getTitle() {
        return title;
    }

    public int getImages() {
        return images;
    }
}
